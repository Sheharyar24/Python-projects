def addition(a, b):
    '''Add two numbers'''
    ans = a + b
    return ans

def subtraction(a, b):
    '''subtract two numbers'''
    ans = a - b
    return ans

def multiplication(a, b):
    '''multiply two numbers'''
    ans = a * b
    return ans

def division(a, b):
    '''divide two numbers'''
    ans = a / b
    return ans
